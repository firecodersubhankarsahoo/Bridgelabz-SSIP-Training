import java.util.*;

public class _9_sum_of_number_until_zero {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while(true){
            int n=sc.nextInt();
            if(n==0){
                break;
            }else{
                System.out.println(n);
            }
        }

    }
}
